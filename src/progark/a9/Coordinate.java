package progark.a9;

/**
 * @author firith
 * This class is done in order to aid
 * the in finding out the position
 * in pixels of the given coordinate
 */
public class Coordinate {
	
	private float x;
	private float y;
	private int coordinateX;
	private int coordinateY;
	
	/**
	 * This Constructor - establish the initial position to 0.
	 */
	public Coordinate(){
		x = 0;
		y = 0;
	}
	
	/**
	 * Constructor - establishes the position
	 * to the given values.
	 * @param x - The position on the x-axis
	 * @param y - The position on the y-axis
	 */
	public Coordinate(float x, float y){
		this.x = x;
		this.y = y;
	}

	/**
	 * Method - Gets the position on the x-axis
	 * of the given coordinate
	 * @return x - The position on the x-axis
	 */
	public float getX() {
		return x;
	}

	/**
	 * Method - Sets the position of the coordinate
	 * on the x-axis according to the given value
	 * @param x - The position on the x-axis.
	 */
	public void setX(float x) {
		this.x = x;
	}

	/**
	 * Method - Gets the position on the y-axis
	 * of the given coordinate
	 * @return y - The position on the y-axis
	 */
	public float getY() {
		return y;
	}

	/**
	 * Method - Sets the position of the coordinate
	 * on the y-axis according to the given value
	 * @param y - The position on the y-axis.
	 */
	public void setY(float y) {
		this.y = y;
	}

	/**
	 * Method - Gets the coordinate on the x-axis.
	 * @return coordinateX - The value of x in the coordinate
	 */
	public int getCoordinateX() {
		return coordinateX;
	}

	/**
	 * Method - Sets the value of the x-axis in the
	 * coordinate
	 * @param coordinateX - The value of the coordinate on the
	 * x-axis
	 */
	public void setCoordinateX(int coordinateX) {
		this.coordinateX = coordinateX;
	}

	/**
	 * Method - Gets the coordinate on the y-axis.
	 * @return coordinateY - The value of y in the coordinate
	 */
	public int getCoordinateY() {
		return coordinateY;
	}

	/**
	 * Method - Sets the value of the y-axis in the
	 * coordinate
	 * @param coordinateY - The value of the coordinate on the
	 * y-axis
	 */
	public void setCoordinateY(int coordinateY) {
		this.coordinateY = coordinateY;
	}
}